//------------------------------------------------------------------------------

class Circle {
      
  private:
    int x,y,radius,col;
    
  public:
    Circle();
    void setCol(int);
    void setCoor(int,int);
    void setR(int);
    void draw();

};

//------------------------------------------------------------------------------

Circle::Circle(){
  x = 0;
  y = 0;
  radius = 0;
  col = WHITE;
}

//------------------------------------------------------------------------------

void Circle::setCol(int a){
  col = a;
}

//------------------------------------------------------------------------------

void Circle::setCoor(int a, int b){
  x = a;
  y = b;
}

//------------------------------------------------------------------------------

void Circle::setR(int a){
  radius = a;
}

//------------------------------------------------------------------------------

void Circle::draw(){
  if(radius>0) circlefill( DoubleBuffer, x, y, radius, col);
}

